  /// Get term by ID
  static Term? getTermById(String id) {
    try {
      return terms.firstWhere((t) => t.id == id);
    } catch (_) {
      return null;
    }
  }

  /// Search terms by title
  static List<Term> searchTerms(String query) {
    if (query.trim().isEmpty) return [];
    final normalizedQuery = query.trim().toLowerCase();
    return terms.where((term) {
      return term.title.toLowerCase().contains(normalizedQuery) ||
          (term.description?.toLowerCase().contains(normalizedQuery) ?? false);
    }).toList();
  }
